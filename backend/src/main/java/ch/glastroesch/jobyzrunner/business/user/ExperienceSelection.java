package ch.glastroesch.jobyzrunner.business.user;

public class ExperienceSelection {
    
    private String key;
    private String value;
    
    public ExperienceSelection(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
    
}
